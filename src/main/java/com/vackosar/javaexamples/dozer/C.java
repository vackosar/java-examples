package com.vackosar.javaexamples.dozer;

public class C {
    private String name;
    private Long account;

    public C() {
    }

    public C(String name, Long account) {
        this.name = name;
        this.account = account;
    }

    public String getName() {
        return name;
    }

    public Long getAccount() {
        return account;
    }
}
